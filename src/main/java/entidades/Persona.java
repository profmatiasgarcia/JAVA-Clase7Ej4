import jakarta.persistence.Column;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.MappedSuperclass;

@MappedSuperclass //para indicar que es una clase de la se heredan otras
public class Persona {

    @Id
    @Column(name = "idPersona")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int idPersona;
    private String apellido;
    private String nombre;
    private String cuit;

    public Persona() {
    }

    //contructor con todos los atributos
    public Persona(String apellido, String nombre, String cuit) {
        this.apellido = apellido;
        this.nombre = nombre;
        this.cuit = cuit;
    }

    public int getIdPersona() {
        return idPersona;
    }

    public void setIdPersona(int idPersona) {
        this.idPersona = idPersona;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCuit() {
        return cuit;
    }

    public void setCuit(String cuit) {
        this.cuit = cuit;
    }

    @Override
    public String toString() {
        return "Persona{" + "idPersona=" + idPersona + ", Apellido=" + apellido + ", Nombre=" + nombre + ", CUIT=" + cuit + '}';
    }

}
